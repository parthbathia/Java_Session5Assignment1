
public class CalculateArea {

	public double calculateRectArea(int rectLength,int rectBreadth){
		
		//Area of Rectangle
		return rectLength*rectBreadth;
		
	}
	
	public double calculateTriArea(int triBase,int triHeight){
		
		//Area of Triangle
		return triBase*triHeight*0.5;
		
	}
	
	
}
